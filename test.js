  콘솔.출력("밀리초 설정 후:", 내날짜2.문자열로());

  // 초 설정 테스트 (현지 시간)
  내날짜2.초설정하기(45, 500);
  콘솔.출력("초 설정 후 (현지 시간):", 내날짜2.문자열로());

  // 분 설정 테스트 (현지 시간)
  내날짜2.분설정하기(30, 45, 300);
  콘솔.출력("분 설정 후 (현지 시간):", 내날짜2.문자열로());

  // 시간 설정 테스트 (현지 시간)
